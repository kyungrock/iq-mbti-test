const QUIZ_ROUND_SIZE = 200;
const MIX_POOL_EXCLUDE = new Set(['mix_200', 'balance', 'slang', 'love_mbti']);

function buildMix200Category() {
  const pool = [];
  Object.values(QUIZ_CATEGORIES).forEach(cat => {
    if (MIX_POOL_EXCLUDE.has(cat.id) || !cat.items?.length) return;
    cat.items.forEach(item => {
      pool.push({ ...item, __cat: cat.id });
    });
  });
  QUIZ_CATEGORIES.mix_200 = {
    id: 'mix_200',
    group: 'trend',
    title: '종합 상식 200',
    icon: '🎯',
    description: '전 분야에서 랜덤 200문항',
    promptLabel: '문제',
    answerLabel: '정답',
    color: '#4ade80',
    roundSize: QUIZ_ROUND_SIZE,
    items: pool
  };
}

function pickQuizRoundItems(category) {
  const size = QUIZ_ROUND_SIZE;
  const pool = category.items.slice();
  if (!pool.length) return [];

  const out = [];
  while (out.length < size) {
    const batch = shuffleArray(pool);
    for (let i = 0; i < batch.length && out.length < size; i++) {
      out.push(batch[i]);
    }
  }
  return out;
}

function applyCategoryRoundDefaults() {
  Object.values(QUIZ_CATEGORIES).forEach(cat => {
    cat.roundSize = QUIZ_ROUND_SIZE;
  });
}

function refreshQuizPools() {
  buildMix200Category();
  applyCategoryRoundDefaults();
}

(function mergeQuizDataExtra() {
  Object.entries(QUIZ_DATA_EXTRA).forEach(([id, items]) => {
    const cat = QUIZ_CATEGORIES[id];
    if (!cat || !items.length) return;
    cat.items.push(...items);
  });
  refreshQuizPools();
})();

function getQuizTotalItemCount() {
  return getQuizCategories().reduce((sum, cat) => sum + cat.items.length, 0);
}
