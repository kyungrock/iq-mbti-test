const QUIZ_GROUPS = [
  { id: 'trend', label: '🔥 요즘 대세' },
  { id: 'love', label: '💕 연애·MBTI' },
  { id: 'balance', label: '⚖️ 밸런스 게임' },
  { id: 'nature', label: '🌿 자연·생물' },
  { id: 'classic', label: '📚 클래식 상식' }
];

function getQuizCategories() {
  return Object.values(QUIZ_CATEGORIES);
}

function getQuizCategoriesByGroup() {
  const grouped = {};
  QUIZ_GROUPS.forEach(g => { grouped[g.id] = []; });
  getQuizCategories().forEach(cat => {
    const g = cat.group || 'classic';
    if (!grouped[g]) grouped[g] = [];
    grouped[g].push(cat);
  });
  return QUIZ_GROUPS.map(g => ({ ...g, categories: grouped[g.id] || [] }))
    .filter(g => g.categories.length > 0);
}

function getQuizCategory(id) {
  return QUIZ_CATEGORIES[id] || null;
}
